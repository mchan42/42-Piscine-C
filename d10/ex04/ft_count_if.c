int		ft_count_if(char **tab, int (*f)(char*))
{
	int		index;
	int		count;

	index = 0;
	count = 0;
	while (tab[index] != '\0')
	{
		if ((*f)(tab[index]) == 1)
			count++;
		index++;
	}
	return (count);
}
